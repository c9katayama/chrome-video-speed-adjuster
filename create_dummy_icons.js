// ダミーのPNGアイコンを作成するスクリプト
const fs = require('fs');
const path = require('path');

// 1x1の透明なPNGファイルのバイナリデータ
// 実際のアプリケーションでは、適切なアイコン画像を使用してください
const TRANSPARENT_PNG = Buffer.from([
  0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00, 0x00, 0x0D,
  0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
  0x08, 0x06, 0x00, 0x00, 0x00, 0x1F, 0x15, 0xC4, 0x89, 0x00, 0x00, 0x00,
  0x0A, 0x49, 0x44, 0x41, 0x54, 0x78, 0x9C, 0x63, 0x00, 0x01, 0x00, 0x00,
  0x05, 0x00, 0x01, 0x0D, 0x0A, 0x2D, 0xB4, 0x00, 0x00, 0x00, 0x00, 0x49,
  0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82
]);

// アイコンサイズのリスト
const iconSizes = [16, 48, 128];

// imagesディレクトリが存在するか確認し、なければ作成
const imagesDir = path.join(__dirname, 'images');
if (!fs.existsSync(imagesDir)) {
  fs.mkdirSync(imagesDir);
}

// 各サイズのアイコンを作成
iconSizes.forEach(size => {
  const iconPath = path.join(imagesDir, `icon${size}.png`);
  fs.writeFileSync(iconPath, TRANSPARENT_PNG);
  console.log(`Created dummy icon: ${iconPath}`);
});

console.log('All dummy icons created successfully.'); 